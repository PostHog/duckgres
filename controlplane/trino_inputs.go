// newTrinoKubeClient builds an in-cluster typed kubernetes.Interface for
// the Trino provisioner's Secret + ConfigMap projections. Returns the
// in-cluster client error verbatim so the caller can log+skip rather
// than fatal-out a non-K8s deployment.
func newTrinoKubeClient() (kubernetes.Interface, error) {
	cfg, err := rest.InClusterConfig()
	if err != nil {
		return nil, fmt.Errorf("in-cluster config: %w", err)
	}
	kc, err := kubernetes.NewForConfig(cfg)
	if err != nil {
		return nil, fmt.Errorf("kubernetes client: %w", err)
	}
	return kc, nil
}

// Env-only knobs that wire the customer-Trino provisioner branch.
//
// Intent inference (no explicit on/off gate):
//
//	Enablement is signaled by setting DUCKGRES_TRINO_COORDINATOR_URL.
//	If the URL is empty, the provisioner branch is off and existing
//	non-MW deployments stay unaffected — exactly the same behavior as
//	the previous DUCKGRES_TRINO_PROVISIONER_ENABLED gate, with one
//	fewer thing for operators to set. If the URL is set, the other
//	required env vars must also be set; missing-required-value errors
//	are fatal at startup (silently no-op'ing wouldn't help — the chart
//	side would be waiting forever for projections that never come).
//
// Credentials are NOT env vars:
//
//	The Trino node-to-node shared secret, the admin password+hash, and
//	the OPA bundle bearer token are self-generated by the provisioner
//	(ensureClusterSecrets) on first startup, with the K8s Secret as the
//	source of truth. The previous DUCKGRES_TRINO_ADMIN_PASSWORD,
//	_ADMIN_PASSWORD_HASH, and _BUNDLE_BEARER_TOKEN env vars existed
//	only to coordinate "same random token in two K8s Secrets" (one for
//	duckgres to validate,
//	one for the chart's sidecar to send). With self-generation, the
//	provisioner writes both Secrets itself — see
//	configstore/trino_cluster_secrets.go.
const (
	// envTrinoCoordinatorURL is the customer-Trino coordinator's REST
	// endpoint, e.g. http://trino-coordinator.trino-customer.svc:8080.
	// Setting this to a non-empty value enables the Trino provisioner
	// branch; leaving it empty disables it. Required configuration when
	// enabled.
	envTrinoCoordinatorURL = "DUCKGRES_TRINO_COORDINATOR_URL"

	// envTrinoIAMAccountID is the AWS account id used to assemble the
	// per-org s3.iam-role property (`arn:aws:iam::<acct>:role/duckling-<orgid>`).
	// Required for AWS deployments: an empty value silently omits the
	// per-catalog IAM role, so every catalog would fall back to the
	// Trino pod's ambient identity — which is the wildcard-assume
	// duckling-* role per the plan's threat model. That collapses the
	// per-tenant S3 boundary. Set DUCKGRES_TRINO_ALLOW_NO_IAM_ROLE=true
	// in dev/test to bypass this gate; never set it in prod.
	envTrinoIAMAccountID = "DUCKGRES_TRINO_IAM_ACCOUNT_ID"

	// envTrinoAllowNoIAMRole is the explicit opt-out for the
	// IAM-required check above. Off by default; setting it to "1" /
	// "true" / "yes" lets buildTrinoWiring proceed with an empty
	// IAM_ACCOUNT_ID. Use ONLY in dev/test where the per-tenant S3
	// isolation isn't exercised. Production deployments must supply
	// DUCKGRES_TRINO_IAM_ACCOUNT_ID instead.
	envTrinoAllowNoIAMRole = "DUCKGRES_TRINO_ALLOW_NO_IAM_ROLE"

	// envTrinoAWSRegion is the AWS region for the per-org IAM role
	// binding and Iceberg catalog config. Falls back to the per-org
	// ManagedWarehouseIceberg.Region when empty.
	envTrinoAWSRegion = "DUCKGRES_TRINO_AWS_REGION"

	// envTrinoNamespace overrides provisioner.TrinoCustomerNamespace
	// for the projected Secrets + ConfigMap. Empty = default
	// ("trino-customer"). Useful for dev clusters that namespace
	// customer Trino differently.
	envTrinoNamespace = "DUCKGRES_TRINO_NAMESPACE"
)

// trinoProvisionerEnabled reports whether the control plane should
// wire the Trino provisioner branch. True iff
// DUCKGRES_TRINO_COORDINATOR_URL is non-empty; the URL doubles as the
// enable signal because operators can't meaningfully use the
// provisioner without it (there'd be nothing to talk to).
func trinoProvisionerEnabled() bool {
	return strings.TrimSpace(os.Getenv(envTrinoCoordinatorURL)) != ""
}

// trinoAllowNoIAMRole reports whether the dev/test bypass for the
// IAM_ACCOUNT_ID gate is set. Off by default — production wiring fails
// if the IAM account id is missing.
func trinoAllowNoIAMRole() bool {
	v := strings.ToLower(strings.TrimSpace(os.Getenv(envTrinoAllowNoIAMRole)))
	return v == "1" || v == "true" || v == "yes"
}

// trinoWiring carries the runtime objects multitenant.go has to mount
// onto the provisioning controller + the API server when the Trino
// branch is enabled. Returned together so the caller doesn't have to
// re-derive any of them.
type trinoWiring struct {
	Provisioner *provisioner.TrinoProvisioner
	BundleStore *opa.BundleStore
	// BundleHandler is the HTTP handler the API server mounts for the
	// customer-Trino OPA sidecar's bundle plugin to poll. Authenticated
	// with the bundle bearer token, which is generated once at cluster
	// bootstrap and stable for the process lifetime (rotation is a
	// follow-up rotation-API concern).
	BundleHandler *opa.Handler
}

// buildTrinoWiring constructs everything the Trino branch needs from
// env vars + the supplied configstore-backed reads. Returns nil when
// the provisioner is disabled (URL not set) and an error when it's
// enabled but the env is incomplete.
//
// The kubernetes.Interface is a typed client suitable for projecting
// Secrets and ConfigMaps into the customer-Trino namespace. In prod
// the caller builds it from rest.InClusterConfig(); in dev/test the
// caller can pass a fake.
//
// The cluster-level credentials (Trino internal-communication shared
// secret, admin password + bcrypt hash, OPA bundle bearer token) are
// NOT read from env — they're self-generated by the provisioner with
// the K8s Secret as the source of truth (a one-bit configstore sentinel
// records "ever bootstrapped"). We run that bootstrap SYNCHRONOUSLY
// here (provisioner.Bootstrap) to obtain the bundle bearer token, then
// construct the bundle HTTP handler with the real token directly.
// Because the handler can't be built until the token is known, there's
// no window where /bundles/trino serves with a placeholder — the
// ordering is enforced by construction. The caller can start the API
// server immediately after this returns.
func buildTrinoWiring(store *configstore.ConfigStore, kc kubernetes.Interface) (*trinoWiring, error) {
	if !trinoProvisionerEnabled() {
		return nil, nil
	}

	coordinatorURL := strings.TrimSpace(os.Getenv(envTrinoCoordinatorURL))
	if coordinatorURL == "" {
		// Belt-and-braces; trinoProvisionerEnabled already gates on this.
		return nil, fmt.Errorf("%s is required to enable the Trino provisioner branch", envTrinoCoordinatorURL)
	}

	// Per-tenant S3 boundary check. An empty IAM_ACCOUNT_ID silently
	// disables the `s3.iam-role` catalog property, collapsing every
	// catalog onto the Trino pod's ambient identity (which is the
	// wildcard-assume duckling-* role per the plan's threat model).
	// That defeats the entire reason for per-org IAM roles. Require the
	// env var unless the operator explicitly opts out for dev/test.
	iamAccountID := strings.TrimSpace(os.Getenv(envTrinoIAMAccountID))
	if iamAccountID == "" && !trinoAllowNoIAMRole() {
		return nil, fmt.Errorf("%s is required when %s is set, or set %s=true to bypass in dev/test (do not bypass in production: empty IAM account id silently collapses the per-org S3 isolation boundary)",
			envTrinoIAMAccountID, envTrinoCoordinatorURL, envTrinoAllowNoIAMRole)
	}

	// Catalog client with empty credentials — Bootstrap (below) calls
	// SetCredentials with the bootstrapped admin pair before the
	// provisioner issues any catalog REST call.
	catalogClient := provisioner.NewTrinoCatalogHTTPClient(coordinatorURL, opa.AdminPrincipal, "")

	bundleStore := &opa.BundleStore{}

	trinoProv, err := provisioner.NewTrinoProvisioner(provisioner.TrinoProvisionerOpts{
		Store:             store,
		BootstrapSentinel: store,
		IcebergStore:      store,
		Kubernetes:        kc,
		Namespace:         strings.TrimSpace(os.Getenv(envTrinoNamespace)),
		Catalog:           catalogClient,
		BundleStore:       bundleStore,
		BundleBuilder:     opa.NewBuilder(),
		IAMAccountID:      iamAccountID,
		AWSRegion:         strings.TrimSpace(os.Getenv(envTrinoAWSRegion)),
	})
	if err != nil {
		return nil, fmt.Errorf("construct Trino provisioner: %w", err)
	}

	// Synchronous bootstrap: generates (first install) or loads the
	// cluster credentials, seeds the catalog client's admin credential,
	// and returns the OPA bundle bearer token. Bounded by a generous
	// timeout — if Postgres or the K8s API server is unreachable for
	// minutes, that's a hard startup failure surfaced in the pod logs.
	bootstrapCtx, cancel := context.WithTimeout(context.Background(), 2*time.Minute)
	defer cancel()
	bundleToken, err := trinoProv.Bootstrap(bootstrapCtx)
	if err != nil {
		return nil, fmt.Errorf("bootstrap Trino cluster secrets: %w", err)
	}

	// Build the handler with the real token directly — no placeholder,
	// no post-construction swap, so no window where the endpoint serves
	// with a token a real client could match by accident.
	bundleHandler := opa.NewHandler(bundleStore, opa.BearerTokenAuth(bundleToken))

	return &trinoWiring{
		Provisioner:   trinoProv,
		BundleStore:   bundleStore,
		BundleHandler: bundleHandler,
	}, nil
}
